model.add(Dense(len(np.unique(y)), activation='softmax'))
model.compile(loss='sparse_categorical_crossentropy', optimizer='adam', metrics=['accuracy'])
model.fit(X_train_scaled, y_train, epochs=50, batch_size=4, verbose=0)

def predict_crop(n, p, k):
    input_npk = np.array([[n, p, k]])
    input_scaled = scaler.transform(input_npk)
    prediction = np.argmax(model.predict(input_scaled), axis=1)
    predicted_crop = label_encoder.inverse_transform(prediction)
    return predicted_crop[0]

@app.route('/', methods=['GET', 'POST'])
def index():
    if request.method == 'POST':
        n = float(request.form['nitrogen'])
        p = float(request.form['phosphorus'])
        k = float(request.form['potassium'])
        best_crop = predict_crop(n, p, k)
        return render_template('index.html', best_crop=best_crop)
    return render_template('index.html', best_crop=None)

